
public class FRAU {

private String haarfarbe;
private String name;
private double koerpergroesse;

public FRAU() {
    
}

public String nenneHaarfarbe() {
    return this.haarfarbe;
}

public String nenneName() {
    return this.name;
}

public double nenneKoerpergroesse() {
    return this.koerpergroesse;
}

public void setzeHaarfarbe(String haarfarbeNeu) {
    this.haarfarbe = haarfarbeNeu;
}

}